$('.exit').click( function () {
	$('.w-fon').show();
	$('.window-exit').show();
});

$('#exit-close').click( function () {
	$('.w-fon').hide();
	$('.window-exit').hide();
});



$('.signup').click( function () {
	$('.w-fon').show();
	$('.window-signup').show();
});

$('#signup-close').click( function () {
	$('.w-fon').hide();
	$('.window-signup').hide();
});
