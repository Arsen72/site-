$('.exit').click( function () {
	$('.w-fon').show();
	$('.window-exit').show();
});

$('#exit-close').click( function () {
	$('.w-fon').hide();
	$('.window-exit').hide();
});



$('.reg').click( function () {
	$('.w-fon').show();
	$('.window-reg').show();
});

$('#reg-close').click( function () {
	$('.w-fon').hide();
	$('.window-reg').hide();
});
